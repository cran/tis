plotWindow <- function(xlim, ylim,
                       log = "", asp = NA,
                       xaxs = "i", yaxs = "i",
                         ...){
  plot.window(xlim = xlim,
              ylim = ylim,
              log = log,
              asp = asp,
              xaxs = xaxs,
              yaxs = yaxs, ...)
}
